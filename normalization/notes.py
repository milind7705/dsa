# No row ordering
# No mixing of datatypes in the dataset
# No table without primary key
# No repeating groups of items in the column

# 2NF
# Each non-key attribute should depend on entire primary key

# 3NF
# Every non-key attribute in the table should depend on the key, the whole key and nothing but the key.

# 4NF
# Multivalued dependency on the table must be multivalued dependency on the key.
